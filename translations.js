window.changeLanguage = function(lang) {
    if (!translations[lang]) return;
    window.currentLang = lang;
    
    // Update all elements using ID mapping
    for (const [id, key] of Object.entries(idMap)) {
        const el = document.getElementById(id);
        if (el && translations[lang][key]) {
            el.innerText = translations[lang][key];
        }
    }

    // Handle RTL languages (Arabic)
    if (lang === 'ar') {
        document.body.dir = 'rtl';
    } else {
        document.body.dir = 'ltr';
    }
    
    // Optional: Log to terminal if exists
    if (typeof logTerminal === 'function') {
        logTerminal(`[SISTEMA] Idioma cambiado a: ${lang.toUpperCase()}`, "#10b981");
    }
};

